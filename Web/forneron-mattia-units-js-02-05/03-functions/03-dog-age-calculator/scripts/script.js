//Start Functions
function calculateDogAge(dogAge, humanAge) {
    console.log("Your dog is "+dogAge*7+" years old in human years!")
    console.log("I'm "+humanAge/7+" years old in dog years!")
}
//End Function


calculateDogAge(3, 21);
calculateDogAge(8, 56);
calculateDogAge(1, 7);

/*
    The result in console-browser:
    Your dog is 21 years old in human years!
    I'm 3 years old in dog years!
    Your dog is 56 years old in human years!
    I'm 8 years old in dog years!
    Your dog is 7 years old in human years!
    I'm 1 years old in dog years!
*/